public class OperationOfComparaison {
    public static void main(String[] args){
        int Num1 = 20 ;
        int Num2 = 12 ;
        if (Num1 == Num2){
            System.out.println("the value of Num1 is equal Num2");
        }
        else if (Num1 != Num2) {
            System.out.println("the value of Num1 is Not equal Num2");
        }
        if (Num1 > Num2) {
            System.out.println("the value of Num1 is great than Num2");
        }
        else if (Num1 < Num2) {
            System.out.println("the value of Num1 is less than Num2");
        }

    }
}
